    private void jTableAttendanceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAttendanceMouseClicked
        try {
            // TODO add your handling code here:
            DefaultTableModel tableAttendance = ( DefaultTableModel) jTableAttendance.getModel();
            int selectedRows = jTableAttendance.getSelectedRow();
            
            txtID.setText(tableAttendance.getValueAt(selectedRows,0).toString());
            txtEMPID.setText(tableAttendance.getValueAt(selectedRows,1).toString());
            txtMONTH.setText(tableAttendance.getValueAt(selectedRows,2).toString());
            
            // convert select query date as string (DOB)
            String strDate = (tableAttendance.getValueAt(selectedRows, 3).toString());
            // To convert the sql date  string format into java date format
            Date dateDOBConvert;
            dateDOBConvert = new SimpleDateFormat("yyyy-MM-dd").parse(strDate);
            txtDate.setDate(dateDOBConvert);
            txtWORKHOURS.setText(tableAttendance.getValueAt(selectedRows,4).toString());
        } catch (ParseException ex) {
            Logger.getLogger(Attendance.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTableAttendanceMouseClicked

