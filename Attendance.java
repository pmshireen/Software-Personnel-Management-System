    public void listAttendance() throws SQLException{
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           dbcon = DriverManager.getConnection(CONSTR,USERNAME,PWD);
           String sqlQuery = "select ID,EMPID,MONTH,DATE,WORKHOURS from spms.attendance order by ID";
           ps = dbcon.prepareStatement(sqlQuery);
           rs = ps.executeQuery();
           System.out.println("DB connected for lisitng attendance");
           ResultSetMetaData stData = rs.getMetaData();
           columnCount = stData.getColumnCount();
           DefaultTableModel tableAttendance = (DefaultTableModel) jTableAttendance.getModel();
           tableAttendance.setRowCount(0);
           
            while (rs.next()) {
                Vector columnData = new Vector();
               
                for (i = 1; i <= columnCount; i++) {
                    columnData.add(rs.getInt("ID"));
                    columnData.add(rs.getInt("EMPID"));
                    columnData.add(rs.getString("MONTH"));
                    columnData.add(rs.getString("DATE"));
                    columnData.add(rs.getString("WORKHOURS"));
                    
                }
                tableAttendance.addRow(columnData);

            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }
    
    
    
